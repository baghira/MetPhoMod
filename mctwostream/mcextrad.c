void Rebin(int no, const float *wco, const float *vo,
	   int nw, const double *wl, double *v)
/* Interpolates a spectrum given by the values wco at the points no to the nw size
   bins with the left delimiters wl. Linear interpolations are used to calculate
   the resulting values
   */
{
  /* Look for the first point. */
  int i, j;
  for (j = 0; j < nw && wl[j] < wco[0]; j++)  v[j] = 0.;
  for (i = 0; wco[i+1] < wl[j]; i++);
  float y, y2;
  y = vo[i] + (wl[j] - wco[i]) / (wco[i+1] - wco[i]) * (vo[i+1] - vo[i]);
  do {
    if (wl[j+1] < wco[i+1]) {
      y2 = vo[i] + (wl[j+1] - wco[i]) / (wco[i+1] - wco[i]) * (vo[i+1] - vo[i]);
      v[j++] = 0.5f * (y + y2);
      y = vo[i] + (wl[j+1] - wco[i]) / (wco[i+1] - wco[i]) * (vo[i+1] - vo[i]);
    }
    else {
      v[j] = 0.5f * (y + vo[i+1]) * (wco[i+1] - wl[j]);
      for (i++; i+1 < no && wl[j+1] > wco[i+1]; i++) {
	v[j] += 0.5f * (vo[i] + vo[i+1]) * (wco[i+1] - wco[i]);
      }
      if (i+1 < no) {
	y = vo[i] + (wl[j+1] - wco[i]) / (wco[i+1] - wco[i]) * (vo[i+1] - vo[i]);
	v[j] += 0.5f * (vo[i] + y) * (wl[j+1] - wco[i]);
	v[j] /= wl[j+1] - wl[j];
	j++;
      }
      else {
	for ( ; j < nw; j++)  v[j] = 0.;
      }
    }
  } while (j < nw);
}

void Interpolate(int no, const float *wco, const float *vo,
		 int nw, const double *wc, double *v)
  /* Interpolates a curve wco/vo onto new x-positions wl. This function
     is similar to Rebin, but it interpolates linearly and not based on
     area. */
{
  int i, j;
  for (i = j = 0; j < nw && wc[j] < wco[i]; j++)  v[j] = 0.;
  for ( ; j < nw; j++) {
    while (i < no && wc[j] > wco[i+1])  i++;
    if (i < no)
      v[j] = (wc[j] - wco[i]) / (wco[i+1] - wco[i]) * (vo[i+1] - vo[i]) + vo[i];
    else
      v[j] = 0.;
  }
}

